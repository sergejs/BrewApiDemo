import SwiftUI

public extension Color {
    static let primaryText = Color("primaryText", bundle: Bundle.module)
    static let secondaryText = Color("secondaryText", bundle: Bundle.module)
    static let tertiaryText = Color("tertiaryText", bundle: Bundle.module)

    static let error = Color("error", bundle: Bundle.module)

    static let neutral1 = Color("neutral1", bundle: Bundle.module)
    static let neutral2 = Color("neutral2", bundle: Bundle.module)

    static let background = Color("primaryBG", bundle: Bundle.module)
    static let primaryBackground = Color("primaryBG", bundle: Bundle.module)
    static let secondaryBackground = Color("secondaryBG", bundle: Bundle.module)
    static let tertiaryBackground = Color("tertiaryBG", bundle: Bundle.module)
}
